// example of for loop in C#
class ForLoop
{
  // main function
  int main()
  {
    // incrementing for loop
    for(int i = 0; i < 10; i++)
    {
      // print the statement "this is a increment for a loop
       Console.WriteLine("This is an increment for loop.");
    }
    
    // decrement by one 
    for(int i = 10; i > 0; i--)
    {
      // print the numbers 1-10
      Console.WriteLine("The is a decrement for loop.");
    }
    
    // increment by the loop for(int i = 1; i < 10; i*2;
    {
      // print 
       Console.WriteLine("This for loop increments by 2.");
    }
    // print the line up to 5
    for (int j = 0; j < 5; j++)
    {
      // print the line number 
      Console.WriteLine("This is line " + j);
    }
    // print the line up to 10 
    for (int k = 0; k < 10; k++)
    {
      // print yhr line number 
      Console.WriteLine("This is line " + k);
    }
  } // end of the program 
}

// C# while loops
System ;
public class WhileLoop
{
   // while loops 
   public static void Main()
   {
      // int to hold the counter
      int i = 5;
      // while the count is larger than 0
      while(i > 0)
      { 
         // write the current count
         System.Console.WriteLine("The current count is greater than 0.");
         // decrement the counter by one 
         i--;
      }
      // Set the j to 0
      int j = 0;
      // run while loop while j is less than 5 
      while(j < 5)
      {
         // print the current count
         System.Console.WriteLine("The current count is lower than 5.");
         // increment the current j by 1 
         j++;
      }
   }
   // end of the program
}

public class Swap
{
   public static void Main()
   {
      // basic program to swap two int values 
    // the initial variables
    int a = 5;
    // the second variable 
    int b = 3;
    // the swap variable
    int swap = 0;
    // print the variables
    Console.WriteLine(a);
      // print the 
    Console.WriteLine(b);
      // swap the variables
      // set the intial variable to swap
    swap = a;
    a = b;
    // end the swap
    b = swap;
    // print the new variables
    Console.WriteLine(a);
      // print variable b 
    Console.WriteLine(b);
      // end of program 
  }
}
